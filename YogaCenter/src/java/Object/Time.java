public class Time {
    private int id_time;
    private String time;

    public Time() {
    }

    public Time(int id_time, String time) {
        this.id_time = id_time;
        this.time = time;
    }

    public int getId_time() {
        return id_time;
    }

    public void setId_time(int id_time) {
        this.id_time = id_time;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
    
}
